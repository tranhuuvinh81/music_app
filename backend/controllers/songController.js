export const updateSong = (req, res) => {
  const { id } = req.params;
  const { title, artist, album, genre, release_year } = req.body;
  let file_url;

  // N·∫øu c√≥ file m·ªõi ƒë∆∞·ª£c upload th√¨ c·∫≠p nh·∫≠t file_url
  if (req.file) {
    file_url = `/uploads/songs/${req.file.filename}`;
  }

  // L·∫•y file_url c≈© n·∫øu kh√¥ng c√≥ file m·ªõi
  db.query("SELECT file_url FROM songs WHERE id = ?", [id], (err, results) => {
    if (err) return res.status(500).json({ error: "L·ªói truy v·∫•n" });
    if (results.length === 0) return res.status(404).json({ message: "Kh√¥ng t√¨m th·∫•y b√†i h√°t" });

    if (!file_url) {
      file_url = results[0].file_url;
    }

    const query = `UPDATE songs SET title=?, artist=?, album=?, genre=?, release_year=?, file_url=? WHERE id=?`;
    db.query(query, [title, artist, album, genre, release_year, file_url, id], (err, result) => {
      if (err) return res.status(500).json({ error: "L·ªói khi c·∫≠p nh·∫≠t b√†i h√°t" });
      if (result.affectedRows === 0) return res.status(404).json({ message: "Kh√¥ng t√¨m th·∫•y b√†i h√°t" });
      res.json({ message: "C·∫≠p nh·∫≠t th√†nh c√¥ng" });
    });
  });
};
